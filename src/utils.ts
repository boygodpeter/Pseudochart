export function escapeHtml(text: string): string {
	return text
		.replace(/&/g, '&amp;')
		.replace(/</g, '&lt;')
		.replace(/>/g, '&gt;')
		.replace(/"/g, '&quot;')
		.replace(/'/g, '&#039;');
}

export function nodeIdStringIsStartOrEnd(nodeId: string): Boolean {
    return nodeId === "Start" || nodeId === "End";
}